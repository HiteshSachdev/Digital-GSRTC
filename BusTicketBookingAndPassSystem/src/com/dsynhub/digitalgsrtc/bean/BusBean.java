package com.dsynhub.digitalgsrtc.bean;

public class BusBean {

	private String busNo;
	private String capacity;
	private String isAvailable;
	
	private String busTypeId;
	private String busTypeName;
	
	private String busCategoryId;
	private String busCategoryName;
	
	private String busDepoId;
	private String busDepoName;
	
	private String cityId;
	private String cityName;

	
	
	public String getBusNo() {
		return busNo;
	}
	public void setBusNo(String busNo) {
		this.busNo = busNo;
	}
	public String getCapacity() {
		return capacity;
	}
	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}
	public String getIsAvailable() {
		return isAvailable;
	}
	public void setIsAvailable(String isAvailable) {
		this.isAvailable = isAvailable;
	}
	public String getBusTypeId() {
		return busTypeId;
	}
	public void setBusTypeId(String busTypeId) {
		this.busTypeId = busTypeId;
	}
	public String getBusTypeName() {
		return busTypeName;
	}
	public void setBusTypeName(String busTypeName) {
		this.busTypeName = busTypeName;
	}
	public String getBusCategoryId() {
		return busCategoryId;
	}
	public void setBusCategoryId(String busCategoryId) {
		this.busCategoryId = busCategoryId;
	}
	public String getBusCategoryName() {
		return busCategoryName;
	}
	public void setBusCategoryName(String busCategoryName) {
		this.busCategoryName = busCategoryName;
	}
	public String getBusDepoId() {
		return busDepoId;
	}
	public void setBusDepoId(String busDepoId) {
		this.busDepoId = busDepoId;
	}
	public String getBusDepoName() {
		return busDepoName;
	}
	public void setBusDepoName(String busDepoName) {
		this.busDepoName = busDepoName;
	}
	public String getCityId() {
		return cityId;
	}
	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	
	
}
